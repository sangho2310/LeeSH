package variable;

public class Var2 {
	public static void main(String[] args) {
		//변수 : 데이터 값을 저장해놓는 공간 (저장공간)
		
		//Java가 변할 수 있기 때문에 변수에다가 저장
		String var1 = "Java"; //var1 이라는 공간을 생성하고 Java라는 데이터를 넣음
		
		System.out.println("Hello "+var1+"!");
		System.out.println(var1+" is Simple");
		System.out.print("안녕하세요");		// ln을 지우면 줄바꿈을 안함
		System.out.println("반갑습니다");
		System.out.println();
		System.out.println(var1+"를 공부합니다.");
		System.out.println(var1);
		
		System.out.println(34-2);
	}
}
