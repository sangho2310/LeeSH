package loop;

public class While1 {
	public static void main(String[] args) {
		// 제어문 : 프로그램의 실행 흐름을 변경하는 문법
		// 조건문 : if ,switch
		// 반복문 : while, for
		
		//while(){}
		//if문과 유사하나 한가지 차이가 있음
		
		//if(){} : ()의 조건이 맞으먄 {} 실행
		//while(){}: ()조건이 맞으면 {} 실행하고 다시 ()조건을 검사하러 위로 올라감
		
		if(true) {
			System.out.println("if");
		}
		
		while(true) {
			System.out.println("while");
		}
	}

}
